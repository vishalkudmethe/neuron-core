// ─── Write Initial Context ────────────────────────────────────────────────────

/// Write the initial `session_context.md` after `neuron init`.
pub async fn write_initial_context(project_root: &Path, manifest: &NeuronManifest) -> Result<()> {
    let context_path = project_root.join(".neuron").join("session_context.md");
    let now          = Utc::now().format("%Y-%m-%d %H:%M UTC").to_string();

    let content = format!(
        r#"# Neuron Session Context
**Project**: {} ({})
**Language**: {}
**Initialized**: {}
**Status**: 🟢 Active — Project just initialized

---

## Current Focus
Project freshly initialized. No files indexed yet.
Run `neuron watch` to begin real-time indexing.

## Open Decisions
- [ ] Confirm primary source directories to watch
- [ ] Set up `.neuronignore` if needed

## Last Git Branch
*(not yet detected — start `neuron watch` to activate git tracking)*

## Next Steps
1. Run `neuron watch` to start indexing
2. Make some commits — Neuron will track them
3. Run `neuron status` to see memory state
4. Run `neuron restore` after any directory change to reload context

---
*Auto-generated by Neuron session engine on init. Run `neuron restore` to reload.*
"#,
        manifest.name,
        manifest.id,
        manifest.language,
        now
    );

    fs::write(&context_path, content).await?;
    Ok(())
}

// ─── Print Status ─────────────────────────────────────────────────────────────

/// Print a compact status view for `neuron status`.
pub async fn print_status(project_root: &Path) -> Result<()> {
    let manifest = NeuronManifest::load(project_root).await?;
    let db_path  = utils::local_db_path(project_root);

    // Count indexed memory units
    let unit_count = if db_path.exists() {
        let pool = search::open_local_db(&db_path).await?;
        let row: (i64,) = sqlx::query_as("SELECT COUNT(*) FROM memory_units")
            .fetch_one(&pool)
            .await
            .unwrap_or((0,));
        row.0
    } else {
        0
    };

    // Git info
    let git_info = git::current_branch(project_root)
        .unwrap_or_else(|_| "unknown".to_string());

    let last_accessed = {
        let dur = Utc::now().signed_duration_since(manifest.last_accessed);
        utils::format_duration(dur)
    };

    let bar = "─".repeat(50);
    println!("\n  {}", bar.dimmed());
    println!(
        "  {} {}  {}",
        "◉".bright_cyan().bold(),
        manifest.name.bold().white(),
        format!("({})", manifest.language).dimmed()
    );
    println!("  {}", bar.dimmed());
    println!("  {:20} {}", "Project ID:".dimmed(),      manifest.id.to_string().yellow());
    println!("  {:20} {}", "Root:".dimmed(),            project_root.display().to_string().cyan());
    println!("  {:20} {}", "Git Branch:".dimmed(),      git_info.bright_yellow());
    println!("  {:20} {}", "Memory Units:".dimmed(),    unit_count.to_string().green().bold());
    println!("  {:20} {}", "Last Accessed:".dimmed(),   last_accessed.white());
    println!("  {:20} {}", "Loop Guard:".dimmed(),      "Active ✓".green());
    println!("  {}\n", bar.dimmed());

    Ok(())
}

