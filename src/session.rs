//! Session context — generates and displays rich project context on restore/startup.
//!
//! On `neuron restore`, this module:
//!   1. Reads the most recent conversation snapshot.
//!   2. Reads the manifest (name, language, last accessed).
//!   3. Reads current git branch + last commit.
//!   4. Writes `session_context.md` for human reference.
//!   5. Prints a rich terminal summary.

use anyhow::Result;
